import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpSession;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/api/error")
public class ErrorProxyController
{

	private static final Logger LOG = LoggerFactory.getLogger(ErrorProxyController.class);

	@RequestMapping(method = RequestMethod.POST, value = "/{medewerkercode}")
	public ResponseEntity<String> stuurErrorMsg(@RequestBody String errorMsg, HttpSession httpSession, @PathVariable String medewerkercode, HttpServletRequest request)
	{
		LOG.error(String.format("Javascript [IP: %s, medewerkerCode: %s]: %s", request.getRemoteAddr(), medewerkercode, errorMsg));
		return ResponseEntity.ok().build();
	}
}
