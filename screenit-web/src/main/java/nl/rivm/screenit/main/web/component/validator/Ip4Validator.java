import java.util.regex.Pattern;

import org.apache.wicket.validation.validator.PatternValidator;

public class Ip4Validator extends PatternValidator
{

	private static final Ip4Validator INSTANCE = new Ip4Validator();

	public static Ip4Validator getInstance()
	{
		return INSTANCE;
	}

	protected Ip4Validator()
	{
		super("^(([01]?\\d\\d?|2[0-4]\\d|25[0-5])\\.){3}([01]?\\d\\d?|2[0-4]\\d|25[0-5])$",
			Pattern.CASE_INSENSITIVE);
	}
}
