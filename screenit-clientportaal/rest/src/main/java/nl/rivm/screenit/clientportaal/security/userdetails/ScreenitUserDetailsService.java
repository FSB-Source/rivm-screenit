import nl.rivm.screenit.service.ClientService;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

@Service
public class ScreenitUserDetailsService implements UserDetailsService
{

	@Autowired
	private ClientService clientService;

	@Override
	public ScreenitUserDetails loadUserByUsername(String bsn) throws UsernameNotFoundException
	{
		return new ScreenitUserDetails(clientService.getClientZonderBezwaar(bsn));
	}
}
